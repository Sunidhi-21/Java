public class Pyramid {

	public static void main(String[] args) {
		int l=1;
		for(int i=5;i>=1;i--)
	     {
			while(l<6)
			{
	         for(int j=1;j<i;j++)
	         {
	             System.out.print(" ");
	         } 
				{
					for(int k=1;k<=l;k++)
						System.out.print("*");
				}System.out.println();
				l++;
			}     
	     } 
	}
}
